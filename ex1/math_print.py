import math


# defining the functions
def golden_ratio():
    """prints golden ration number (first requirement)"""
    print((1 + math.sqrt(5)) / 2)


def square_five():
    """prints 5 square 2 (second requirement)"""
    print(5**2)


def hypotenuse():
    """prints hypotenuse of triangle with beams of 4 and 5"""
    print(math.sqrt(4**2 + 5**2))


def pi():
    """prints the value of pi (forth requirement)"""
    print(math.pi)


def e():
    """prints the number e (5th requirement)"""
    print(math.e)


def square_area():
    """prints values of square areas from 1 to 10 (sixth requirement)"""
    print(1**2, 2**2, 3**2, 4**2, 5**2, 6**2, 7**2, 8**2, 9**2, 10**2)
